internal static class WellKnownChars
{
    /// <summary>
    /// Open and close brackets.
    /// </summary>
    internal const char OpenBracket = '[';

    /// <summary>
    /// Open and close brackets.
    /// </summary>
    internal const char CloseBracket = ']';

    /// <summary>
    /// Open and close braces.
    /// </summary>
    internal const char OpenBrace = '{';

    /// <summary>
    /// Open and close braces.
    /// </summary>
    internal const char CloseBrace = '}';

    /// <summary>
    /// Open and close parenthesis.
    /// </summary>
    internal const char OpenParen = '(';

    /// <summary>
    /// Open and close parenthesis.
    /// </summary>
    internal const char CloseParen = ')';

    /// <summary>
    /// Open and close angle brackets.
    /// </summary>
    internal const char OpenAngle = '<';

    /// <summary>
    /// Open and close angle brackets.
    /// </summary>
    public const char CloseAngle = '>';
}
